console.log("Control Statements");
//if statement
let a=10;
let b=20;
if(!(b>a)){  // ! means negation
    console.log("b is greater than a");
}
else{
    console.log("a is greater than b");
}

console.log("For Loop");
for(let i=0;i<10;i++){
    console.log(i);
}

console.log("While Loop");
let i=0;
while(i<10){
    console.log(i);
    i++;
}

console.log("Do-While Loop");
let j=0;
do{
    console.log(j);
    j++;
}while(j<10)